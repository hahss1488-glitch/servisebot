    allowed_chars = RUS_LETTERS + '0123456789'
    normalized = ''.join([c for c in normalized if c in allowed_chars])
    
    # 5. –ê–≤—Ç–æ–¥–æ–±–∞–≤–ª–µ–Ω–∏–µ —Ä–µ–≥–∏–æ–Ω–∞ –µ—Å–ª–∏ –Ω—É–∂–Ω–æ
